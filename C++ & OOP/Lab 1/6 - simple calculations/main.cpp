#include <iostream>

using namespace std;

int main()
{
    int num1, num2;
    cout << "Enter number1: ";
    cin >> num1;
    cout << "Enter number2: ";
    cin >> num2;

    cout << "Sum of number1 " << num1 << " and " << num2 << " number2 is: " << num1 + num2 <<endl;
    cout << "subtract of number1 " << num1 << " and " << num2 << " number2 is: " << num1 - num2 <<endl;
    cout << "multiply of number1 " << num1 << " and " << num2 << " number2 is: " << num1 * num2 <<endl;
    cout << "divide of number1 " << num1 << " and " << num2 << " number2 is: " << num1 / num2 <<endl;

    return 0;
}
